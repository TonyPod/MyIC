        public static string[] GetPolyphone(char c)
        {
            List<string> list = new List<string>();

            for (int j = 0; j < _Allhz.Length; j++)
            {
                if (_Allhz[j][1].IndexOf(c) != -1)
                {
                    list.Add(_Allhz[j][0]);
                }
            }
            return list.ToArray();
        }
